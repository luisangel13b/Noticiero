                        <p><font size="4">

El estado de Nayarit regresará oficialmente al semáforo epidemiológico amarillo el próximo lunes, como resultado de que sus autoridades han confirmado recientemente hasta 30 contagios de Covid-19 diarios, luego que su gobierno estatal llegó a certificar uno al día hace dos semanas.

Este jueves, los integrantes de la Mesa Covid nayarita determinaron prohibir los jaripeos, fiestas patronales, bailes y reuniones masivas en general, en un intento por frenar el avance de la pandemia en el estado.

Antonio Serrano, secretario general de Gobierno, explicó que asimismo se buscará “mantener la economía actuante”, de tal manera que “las unidades económicas o empresariales” operarán al 75 por ciento de sus capacidades. “Los horarios para restaurantes no serán recortados, sólo su capacidad de recibir comensales”, agregó.

Destacó que si bien se incrementaron los contagios de coronavirus, no aumentaron las defunciones ni el número de personas hospitalizadas.

Asimismo, Serrano Guzmán dio a conocer que si bien no se pidió a la Secretaría de Educación de Nayarit cancelar sus eventos de clausura de cursos, se le recomendó acoplarse a las medidas sanitarias ya conocidas, y “que si van a invitar a los dos papás del graduado, del niño o la niña, que solamente vaya uno y que sea en lugares abiertos”.<br></font></p>
					
