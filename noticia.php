						Amado Nervo fueron atacados por un grupo de sujetos que se desplazaban en una camioneta en color blanco; en ese sitio dos elementos de la Guardia Nacional resultaron heridos, uno de ellos de gravedad, por lo que tuvo que ser trasladado de emergencia a un hospital, donde más tarde perdió la vida, el estado de salud del otro fue reportado como delicado.</font></p>
